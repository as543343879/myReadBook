class Solution448 {
    public List<Integer> findDisappearedNumbers(int[] nums) {
        int n = nums.length;
        int[] dict = new int[n + 1];
        for(int t : nums) {
            dict[t] ++;
        }
        List<Integer> res = new ArrayList<>();
        for(int i = 1; i <= n; i ++ ) {
            if(dict[i] == 0) {
                res.add(i );
            }
        }
        return res;
    }
}
