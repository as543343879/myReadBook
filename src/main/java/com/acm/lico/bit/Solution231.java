class Solution231 {
    public boolean isPowerOfTwo(int n) {
        return n > 0 &&( n & (n-1)) == 0;
    }
    public boolean isPowerOfTwo2(int n) {
        if(n == 1) {
            return true;
        } else if( n == 0) {
            return false;
        }
        if(n % 2 != 0) {
            return false;
        }
        n /= 2;
        return isPowerOfTwo(n);
    }

}
