class Solution416 {
    public boolean canPartition(int[] nums) {
        boolean res = false;
        if(nums.length == 1) {
            return res;
        }
        int max = 0;
        int sum  = 0;
        for(int num: nums) {
            max = Math.max(num,max);
            sum += num;
        }
        if(sum % 2 != 0) {
            return res;
        }
        sum /= 2;
        if(max  > sum) {
            return res;
        } else if(max == sum) {
            return true;
        }
        int len = nums.length;
        boolean[][] dp = new boolean[len][sum+1];
        // ä¼˜åŒ–ä»£ç  j == num[i] çš„æƒ…å†µ
        dp[0][0] = true;
        if(nums[0] <= sum) {
            dp[0][nums[0]] = true;
        }
        for(int i = 1; i < len; i ++) {
            for(int j = 0; j <= sum; j ++) {
                dp[i][j] = dp[i-1][j];
                if(nums[i] <= j) {
                    dp[i][j] = dp[i-1][j] || dp[i-1][j-nums[i]];

                }

            }
            if(dp[i][sum]) {
                return true;
            }
        }
        return dp[len-1][sum];

    }
}
