package com.acm.lico.offer68;



public class TreeNode {
    public  int val;
    public  TreeNode left;
     public TreeNode right;
    public  TreeNode(int x) { val = x; }

        TreeNode() {}
      TreeNode(int val, TreeNode left, TreeNode right) {
          this.val = val;
          this.left = left;
          this.right = right;
      }
}